package com.openexchange.groupware.infostore.ajax;

public class AJAXException extends Exception {

	public AJAXException() {
		super();
	}

	public AJAXException(String arg0) {
		super(arg0);
	}

	public AJAXException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AJAXException(Throwable arg0) {
		super(arg0);
	}

}
